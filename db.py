def clear_all_entries():
    """Полностью очищает таблицу notebook_entries"""
    conn = sqlite3.connect('gto.db')
    cursor = conn.cursor()

    try:
        cursor.execute('DROP TABLE IF EXISTS notebook')
        cursor.execute('''
        CREATE TABLE notebook (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            exercise TEXT NOT NULL,
            result TEXT NOT NULL,
            notes TEXT,
            date TEXT NOT NULL
        )
        ''')

        conn.commit()
    except Exception as e:
        conn.rollback()
    finally:
        conn.close()



def init_db():
    """Инициализация БД с созданием таблиц и заполнением нормативов"""
    conn = sqlite3.connect("gto.db")
    cursor = conn.cursor()

    # Таблица нормативов (все ступени в одной таблице)
    cursor.execute("""
    CREATE TABLE IF NOT EXISTS standards (
        id INTEGER PRIMARY KEY,
        exercise TEXT NOT NULL,
        type INT NOT NULL,
        gender TEXT NOT NULL,
        stage INTEGER NOT NULL,
        gold REAL NOT NULL,
        silver REAL NOT NULL,
        bronze REAL NOT NULL,
        unit TEXT NOT NULL,
        category TEXT NOT NULL
    )
    """)


    # Таблица записей в блокноте
    cursor.execute("""
    CREATE TABLE IF NOT EXISTS notebook (
        id INTEGER PRIMARY KEY,
        exercise TEXT NOT NULL,
        result REAL NOT NULL,
        notes TEXT,
        date TEXT NOT NULL
    )
    """)

    # Заполняем нормативы (актуальные данные 2024)
    _fill_standards(cursor)

    conn.commit()
    conn.close()


def save_entry(exercise, result, notes, date):
    conn = sqlite3.connect('gto.db')
    cursor = conn.cursor()

    cursor.execute('''
    INSERT INTO notebook (exercise, result, notes, date)
    VALUES (?, ?, ?, ?)
    ''', (exercise, result, notes, date))

    conn.commit()
    conn.close()


def delete_entry(entry_id):
    """Удаление записи по ID"""
    conn = sqlite3.connect('gto.db')
    cursor = conn.cursor()

    cursor.execute('DELETE FROM notebook WHERE id = ?', (entry_id,))

    conn.commit()
    conn.close()


def get_user_entries_by_exercise(exercise_name):
    """Получает все записи пользователя по названию упражнения"""
    conn = sqlite3.connect('gto.db')
    cursor = conn.cursor()

    cursor.execute('''
    SELECT id, exercise, result, notes, date 
    FROM notebook 
    WHERE exercise = ?
    ORDER BY date DESC
    ''', (exercise_name,))

    entries = []
    for row in cursor.fetchall():
        entries.append({
            'id': row[0],
            'exercise': str(row[1]),
            'result': float(row[2]) if row[2] else 0,
            'notes': str(row[3]) if row[3] else "",
            'date': str(row[4])
        })

    conn.close()
    return entries


def get_required_categories(stage, gender):#for calculator
    """Получает список обязательных категорий для ступени и пола"""
    conn = sqlite3.connect('gto.db')
    cursor = conn.cursor()

    cursor.execute('''
    SELECT DISTINCT category 
    FROM standards 
    WHERE stage = ? AND gender = ? AND type = 1
    ''', (stage, gender))

    categories = [row[0] for row in cursor.fetchall()]
    conn.close()
    return categories


